def sum(number1, number2):
    return number1 + number2


def subtraction(number1, number2):
    return number1 - number2


def multiply(number1, number2):
    return number1 * number2


def divide(number1, number2):
    if (number2 != 0):
        return number1 / number2
    else:
        return "It isnt possible divide by zero"
